// Write a program  to print all the ASCII codes and their equivalent characters with the ASCII codes from 48 to 127.
#include <iostream>
using namespace std;
int main()
{
	for(int i = 48; i <= 127; i++) {
		cout << i << ": " << (char)i << endl;
	}
	return 0;
}
